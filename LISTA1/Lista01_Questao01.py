## Faça um programa que peça dois números inteiros e imprima a soma desses dois números 
n1=int(input("Digite o primeiro número inteiro: "))
n2=int(input("Digite o segundo número inteiro: "))
print("A soma de", n1, "com", n2, "é equivalente a: ",n1+n2)

#Explicação do código:
#As variáveis n1 e n2 guardam números selecionados pelo usuário(input) em valores
#inteiros(int)
#e o comando 'print()' exibe ambas as variáveis e soma as duas pela fórmula
#n1+n2
